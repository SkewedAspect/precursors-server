-export([create_ets/0]).

%% --------------------------------------------------------------------------------------------------------------------
%% API
%% --------------------------------------------------------------------------------------------------------------------

create_ets() ->
	% ETS table that stores current physical state (#pre_physical{}) of all entities.
	ets:new(?MODULE, [public, named_table, {keypos, 2}]).

